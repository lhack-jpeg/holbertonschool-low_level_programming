#include "3-calc.h"
/**
 * op_add - returns sum of a + b
 * @a: int
 * @b: int
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of a & b
 * @a: int
 * @b: int
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product of a & b
 * @a: int
 * @b: int
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns division of a & b
 * @a: int
 * @b: int
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns sum of a + b
 * @a: int
 * @b: int
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
